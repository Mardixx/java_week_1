public class ifStatements {

    public static void main(String[] args) {

        int age = 25;

        if (age >= 75) {
            System.out.println("OK Boomer");
        } else if (age >= 18) {
            System.out.println("You are old enough");
        }
        else {
            System.out.println("You are not old enough");
        }

    }

}